def get_data(E_incident_MeV, angle_deg, source="auto"):
    try:
        if source in ("baba", "auto"):
            return get_baba_exfor_data(E_incident_MeV, angle_deg)
    except KeyError:
        pass

    try:
        if source in ("endf", "auto"):
            e, y = get_endf81_data(E_incident_MeV, angle_deg)
            return e, y, None
    except KeyError:
        pass

    return None


